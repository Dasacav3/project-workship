# Install Node.js dependencies

## Install fnm (Fast Node Manager)

# Install fnm using winget (Windows package manager)
winget install Schniz.fnm

# Install fnm using curl and bash (Linux/Unix)
curl -fsSL https://fnm.vercel.app/install | bash

# Install Node.js using fnm
fnm install 20

# Set Node.js version as default
fnm default 20

# Update npm
npm install -g npm

# Install Pnpm
npm install -g pnpm
